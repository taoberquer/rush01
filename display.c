#include "fcts.h"

void 	display(t_view ***tab)
{
	char	c;
	int		i;
	int		j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			c = tab[i][j]->value + '0';
			write(1, &c, 1);
			if (j != 3)
				write(1, " ", 1);
			++j;
		}
		write(1, "\n", 1);
		++i;
	}
}
